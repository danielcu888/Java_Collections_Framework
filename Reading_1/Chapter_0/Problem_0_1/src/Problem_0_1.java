public class Problem_0_1 
{
	public static void main(String args[])
	{
		System.out.println(Integer.MAX_VALUE); //max value of 32-bit int in twos-complement notation is 2^(32-1) - 1
		System.out.println(Integer.MAX_VALUE + 1); //no positive decimal value can give the binary equivalent of this twos-complement binary
		System.out.println(Math.abs(Integer.MIN_VALUE)); //-2^(n-1) standard twos-complement result
	}
}
